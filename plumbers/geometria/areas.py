def area_quadrado1(lado):
    '''
    Calcula a área de um quadrado.
    Lado * Lado
    '''
    return lado * lado 

def area_triangulo1(base, altura):
    '''
    Calcula a área de um triângulo.
    (base * altura) / 2
    '''
    return (base * altura) / 2

def area_retangulo1(base, altura):
    '''
    Calcula a área de um retângulo.
    base * altura
    '''
    return base * altura